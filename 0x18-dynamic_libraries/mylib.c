#include <stdio.h>
#include "main.h"

/**
 * add- to add two numbers
 * @a: first integer
 * @b: second integer
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub- to substruct
 * @a: first integer
 * @b: second integer
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul- to multiply
 * @a: fist inetger
 * @b: second integer
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div- perform division
 * @a: first integer
 * @b: second integer
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod- Modulus
 * @a: first integer
 * @b: second intger
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
